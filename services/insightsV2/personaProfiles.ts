import type { PersonaProfileCopy, SignalSentiment } from './types';

type PersonaProfileSource = Omit<PersonaProfileCopy, 'polarity'> & {
  polarity?: SignalSentiment;
};

const NEGATIVE_PERSONA_CATEGORIES = new Set<PersonaProfileCopy['category']>([
  'boundariesSelfTrust',
  'familyHome',
  'griefTransitions',
  'relationships',
  'restCapacity',
  'safetyRegulation',
  'scarcityAbundance',
  'selfWorthReceiving',
]);

const POSITIVE_PERSONA_CATEGORIES = new Set<PersonaProfileCopy['category']>([
  'glimmersRegulation',
]);

const PERSONA_RECOVERY_SIGNALS: PersonaProfileCopy['blockedRecoverySignals'] = [
  'baseline_recovery',
  'body_lightness',
  'care_before_owed',
  'connection_glimmer',
  'easy_rest',
  'familiar_calm',
  'glimmer_softening',
  'ordinary_downtime',
  'quiet_safety',
  'receiving_openness',
  'rest_as_care',
  'rest_without_guilt',
  'restorative_moment',
  'restorative_pause',
  'small_relief_signals_change',
  'support_abundance_shift',
  'unbraced_stillness',
];

function inferPersonaPolarity(profile: PersonaProfileSource): SignalSentiment {
  if (profile.polarity) return profile.polarity;
  if (POSITIVE_PERSONA_CATEGORIES.has(profile.category)) return 'positive';
  if (NEGATIVE_PERSONA_CATEGORIES.has(profile.category)) return 'negative';
  return 'mixed';
}

function completePersonaProfile(profile: PersonaProfileSource): PersonaProfileCopy {
  const polarity = inferPersonaPolarity(profile);

  return {
    ...profile,
    polarity,
    blockedRecoverySignals:
      profile.blockedRecoverySignals ??
      (polarity === 'negative' ? PERSONA_RECOVERY_SIGNALS : undefined),
    requiredSignalRoles:
      profile.requiredSignalRoles ??
      (polarity === 'negative'
        ? ['feeling_state', 'body_signal', 'protective_strategy', 'relational_context', 'cognitive_process']
        : undefined),
  };
}

